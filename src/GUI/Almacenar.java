    private void jComboBoxEmpaqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxEmpaqueActionPerformed
        // TODO add your handling code here:
        int selecccion=this.jComboBoxEmpaque.getSelectedIndex();
        if(selecccion==1){
            this.jComboBoxBetallable.setEnabled(false);
            visibilidadComponentes(false);
            this.jComboBoxBetallable.setSelectedIndex(0);
        }
        if(selecccion==0){
            this.jComboBoxBetallable.setEnabled(true);
        }
    }//GEN-LAST:event_jComboBoxEmpaqueActionPerformed

