    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
        String nombre=this.jTextFieldNombre.getText();
        int stock=Integer.parseInt(this.jTextFieldStock.getText());
        float precio=Float.parseFloat(this.jTextFieldPrecio.getText());
        String empaque=this.jComboBoxEmpaque.getSelectedItem().toString();
        int det=this.jComboBoxBetallable.getSelectedIndex();
        float precioUnidad=Float.parseFloat(this.jTextFieldPrecioUnidad.getText());
        int unidades=Integer.parseInt(this.jTextFieldUnidades.getText());
        boolean detallable=false;
        
        if(det==1)
            detallable=true;
        try {
            world.crear_nuevo_producto(nombre, stock, empaque, detallable, precioUnidad, unidades, precio);
        } catch (SQLException ex) {
            Logger.getLogger(Almacenar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

